num = 100
print(f'Num: {num}  bin: {bin(num)}  Oct: {oct(num)}  Hex: {hex(num)}')
num = 0b100 
print(f'Num: {num}  bin: {num:b}  Oct: {num:o}  Hex: {num:x}')
num = 0o100
print(f'Num: {num}  bin: {num:b}  Oct: {num:o}  Hex: {num:x}')
num = 0x100
print(f'Num: {num}  bin: {num:b}  Oct: {num:o}  Hex: {num:x}')